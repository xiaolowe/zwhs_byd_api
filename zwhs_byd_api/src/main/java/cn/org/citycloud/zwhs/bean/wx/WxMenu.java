package cn.org.citycloud.zwhs.bean.wx;

import java.util.ArrayList;
import java.util.List;

public class WxMenu {
	
	private String title;
	
	private String type;
	
	private String text;
	
	private WxLink link;
	
	private List<Asset> assets = new ArrayList<Asset>();
	
	private List<WxMenu> submenu = new ArrayList<WxMenu>();

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getText() {
		return text;
	}

	public void setText(String text) {
		this.text = text;
	}

	

	public List<Asset> getAssets() {
		return assets;
	}

	public void setAssets(List<Asset> assets) {
		this.assets = assets;
	}

	public List<WxMenu> getSubmenu() {
		return submenu;
	}

	public void setSubmenu(List<WxMenu> submenu) {
		this.submenu = submenu;
	}

	public WxLink getLink() {
		return link;
	}

	public void setLink(WxLink link) {
		this.link = link;
	}


}
